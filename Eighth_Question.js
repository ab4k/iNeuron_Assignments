// 08. Demonstrate the use of trim() to remove any trailing whitespace at the beginning and the end of a string.

let nameWithWhitespaces = "Shiva   "; // Contains some whitespaces
console.log("Length of name with whitespaces:", nameWithWhitespaces.length);

console.log(
  "After using trim() function the length without whitespaces is:",
  nameWithWhitespaces.trim().length
);
